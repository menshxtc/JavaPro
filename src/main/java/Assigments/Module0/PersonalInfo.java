package Assigments.Module0;

public class PersonalInfo {
    public static void main(String[] args) {
        // 输出个人信息头部
        System.out.println("+----------------------+");
        System.out.println("| Personal Information |");
        System.out.println("+----------------------+");
        System.out.println();

        // 输出个人信息
        System.out.println("Name: Mensh");
        System.out.println("Major: Cyberspace Security");
        System.out.println("Favorite Color: Blue");
        System.out.println();

        // 输出三个有趣的事实
        System.out.println("Three Interesting Facts:");
        System.out.println("1. I have a apple.");
        System.out.println("2. I can speak 5 languages.");
        System.out.println("3. I play basketball well.");
        System.out.println();

        // 输出尾部
        System.out.println("+--------------------+");
        System.out.println("| End of Information |");
        System.out.println("+--------------------+");
    }
}
